package ch01_start;

public class HelloWorld {

    // main 메소드(main 치고 자동완성)
    public static void main(String[] args) {
        // 코드 실행 [Shift + F10]
        // main 메소드 내에 존재하는 코드가 실행이 된다.

        // 코드라인 복사 [Ctrl + D]
        System.out.println("Hello World!!");
        System.out.println("Hello World!!");
        System.out.println("Hello World!!");
        // 코드라인 삭제 [Ctrl + Y]
        // 실행취소 [Ctrl + Z]
        // 실행취소의 취소 [Ctrl + Shift + Z]
        // 코드 이동 [Ctrl + Shift + 방향키(위 또는 아래)

        // 자동완성 [Ctrl + Space]
        System.out.println();

        // so 입력 후 엔터
        System.out.println();


    }

}
